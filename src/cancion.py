class Cancion:

    def __init__():
        self.artista=""
        self.titulo=""
        self.fecha=""
        self.genero=""
        self.numTrack=""

    def setArtista(artista):
        self.artista=artista

    def setTitulo(titulo):
        self.titulo=titulo

    def setFecha(fecha):
        self.fecha=fecha

    def setGenero(genero):
        self.genero=genero

    def setTrack(track):
        self.numTrack=track
